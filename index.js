function run(char){
    let guess = Input.value;
    Input.value = "";
    const newguess = document.createElement("div");
    newguess.className = "newguess";
    for (let i = 0; i<8; i++){
        const catdiv = document.createElement("div");
        if (i == 0){
            catdiv.classList.add("picture");
        }
        else{
            catdiv.classList.add('fade-in');
            catdiv.style.animationDelay = `${(i-1) * 0.7}s`;
        }
        catdiv.classList.add("box");
        char[i].forEach(pow => {
            catdiv.innerHTML += pow + "<br>";
        });
        if (char[i].length == randomChar[i].length){
            let fornow = true;
            char[i].forEach(pow => {
                if (!randomChar[i].includes(pow)){
                    fornow = false;
                }
            });
            if (fornow){
                catdiv.classList.add("correct");
            }
        }
        if (!catdiv.classList.contains("correct")){
            char[i].forEach(pow => {
                if (randomChar[i].includes(pow)){
                    catdiv.classList.add("half");
                }
            });
            if (!catdiv.classList.contains("half")){
                catdiv.classList.add("wrong");
            }
        }
        if (char[i].length > 5){
            catdiv.classList.add("toolong")
        }
        newguess.appendChild(catdiv);
    }
    if (GameArea.children.length > 1){
        GameArea.insertBefore(newguess,GameArea.children[1])
    }
    else{
        GameArea.appendChild(newguess);
    }
    if (char == randomChar){
        setTimeout(() => {Win(guess);}, 5000);
    }
};
